package homework_1;

public interface BasitHesapMakine {
	
	static double toplama(double a,double b) {
		return a+b;
	}
	
	static double cikarma(double a,double b) {
		return a-b;
	}
	
	static double carpma(double a,double b) {
		return a*b;
	}
	
	static double bolme(double a,double b) {
		return a/b;
	}

}
